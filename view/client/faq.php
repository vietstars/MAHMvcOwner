			<h2 id="bags" class="cat-title text-center">Messenger Bags</h2>
			<h6>What size laptop fits in the bag?</h6>
			This bag can accommodate laptops up to 15” without a case or 13″ with a case. Of course smaller laptops and tablets will comfortably fit in the rear compartment as well.

			<h6>The leather seems stiff at first, will it soften up?</h6>
			We use full-grain premium 6-7 oz. leather sourced right here in the USA. It may seem a bit stiff at first but will quickly soften with some use and wear. The beautiful aspect of the leather is that it will conform to your individual use and get better with age.

			<h6>Where is the messenger bag made?</h6>
			Each bag is handcrafted in Portland, Oregon and all of the fabrics, leather, and hardware are sourced from the USA to ensure the highest quality possible.

			<h6>How should I care for the bag?</h6>
			Waxed cotton canvas is a rugged water-resistant material and can be used for years without washing. Waxed canvas should not be machine washed as the detergent and washing will deteriorate the fabric. To spot clean dirty areas, simply use a damp cloth and let air dry.

			<h6>Is the bag waterproof?</h6>
			The combination of the exterior waxed canvas and interior Cordura nylon lining make the bag very water resistant but the bag is not 100% waterproof. Your gear should stay dry even if you get caught in the rain, but we recommend keeping the bag in a dry place when possible.

			<h2 id="leather" class="cat-title text-center">Leather Goods</h2>
			<h6>What type of leather do you use?</h6>
			The premium full-grain English Bridle leather sourced from Wickett & Craig, one of the leading tanneries in the world that’s been producing leather for over 120 years. This leather is soft to the touch yet super durable and will develop a beautiful patina over time with use and wear.

			<h6>The cards are a little hard to take in and out of the wallet, will it stretch out?</h6>
			Yes, the card slots are designed to fit the exact size of a credit card so they may seem tight at first. Over time the leather will stretch out with use and the cards will be much easier to get in and out of the wallet.

			<h6>My leather mousepad is warping, what should I do?</h6>
			Since leather is a natural material, it can tend to warp a little bit. To get the mousepad to lay flat, simply work the leather with your hands bending it the opposite direction that it is curling. The more it is handled the more pliable it becomes.

			<h6>How can I prevent my leather mousepad from slipping on my desk?</h6>
			If your desk is made from a slippery material like glass, your mousepad may move around a little bit. To prevent this, you can adhere a small piece of double-sided tape or purchase small rubber feet like these.

			<h6>What should I do to care for my leather products?</h6>
			Leather will absorb the oils from your hands and may not need any maintenance. If the leather seems to be drying out, you can condition it with mink oil or neatsfoot oil. Apply a light coat of oil and work it into the leather using your hands or a dry cloth. Allow leather to dry before using.

