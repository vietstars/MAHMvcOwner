			<h6>How does your clothing fit?</h6>
			Please refer our Size & Fit page for details about each of our products.

			<h6>I ordered two shirts of the same size, why is one smaller?</h6>
			Some of the designs are printed on different style shirts. Check the product description for any special sizing notes or refer to our Size & Fit page here.

			<h6>How should I care for my tees, crewnecks, and hoodies? Will they shrink?</h6>
			We recommend turning them inside out and machine or hand washing on cold. Since all of the shirts have some percentage of cotton, they will tend to shrink a little after being washed and dried. To minimize the shrinking, machine dry low or hang dry.

			<h6>What type of printing process is used on your clothing?</h6>
			All of our shirts and hoodies are screenprinted. Most of the shirts are printed with water-based or discharge ink which leaves an extremely soft print. A few of the shirts use a soft-hand plastisol ink to get a bright white print. Check the product pages for the specific type of printing for each individual product.

			<h6>Will the printing on the tees fade?</h6>
			We use high quality water-based inks to produce the softest printing possible. Some of the printing may fade a little after multiple washes.

			<h6>Do you manufacture your products in-house?</h6>
			While we do all of our design in-house, we don’t do our own manufacturing. We partner with great manufacturers all across the US for each of our products.

			<h6>Can you notify me when a product is back in stock?</h6>
